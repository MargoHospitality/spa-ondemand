export function t(key: string, locale: Locale = 'fr', vars?: Record<string, string>): string {
  let text = translations[locale]?.[key] || translations.fr[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}

export const LocaleContext = createContext<{ locale: Locale; setLocale: (l: Locale) => void }>({
  locale: 'fr',
  setLocale: () => {},
});

export function useLocale() {
  return useContext(LocaleContext);
}

export function useT() {
  const { locale } = useLocale();
  return (key: string, vars?: Record<string, string>) => t(key, locale, vars);
}
